public final class GalleryUtil {

    private static final String TAG = "GalleryUtil";

    private GalleryUtil() {
        //no instance
    }

    /**
     * 保存图片到指定路径
     *
     * @param context  当前所在 Activity
     * @param bmp      Bitmap 对象
     * @param savePath 保存路径
     */
    public static void saveImageToGallery(Context context, Bitmap bmp, String savePath) {
        // 首先保存图片
//        File appDir = new File(Environment.getExternalStorageDirectory(), "keen");
        File appDir = new File(savePath);
        if (!appDir.exists()) {
            appDir.mkdir();
        }
        String fileName = System.currentTimeMillis() + ".jpg";
        File file = new File(appDir, fileName);
        try {
            FileOutputStream fos = new FileOutputStream(file);
            bmp.compress(Bitmap.CompressFormat.JPEG, 100, fos);
            fos.flush();
            fos.close();
        } catch (FileNotFoundException e) {
            e.printStackTrace();
        } catch (IOException e) {
            e.printStackTrace();
        }

        // 其次把文件插入到系统图库
        try {
            MediaStore.Images.Media.insertImage(context.getContentResolver(),
                    file.getAbsolutePath(), fileName, null);
        } catch (FileNotFoundException e) {
            e.printStackTrace();
        }
        // 最后通知图库更新
        context.sendBroadcast(new Intent(Intent.ACTION_MEDIA_SCANNER_SCAN_FILE, Uri.parse("file://" + file)));
    }

}
